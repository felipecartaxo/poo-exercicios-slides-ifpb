
public class StringConcatenacao {

	public static void main(String[] args) {
		String nome = "joao" + "cruz";
		String termo = "2" + "2";
		String local = "Sala " + 100;
		
		System.out.println(nome); // "joao cruz"
		System.out.println(termo); // "22"
		System.out.println(local); // "Sala 100" 
	}
}